    public double[] crearAuxiliar(double pesos[]){
        double auxiliar[] = new double[pesos.length];
        for (int i = 0; i < auxiliar.length; i++) {
            auxiliar[i] = ((int)(pesos[i]*10000))/10000.0;            
        }
        return auxiliar;
    }
