    private void btnDianosticarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDianosticarActionPerformed
        // TODO add your handling code here:
        try{
            double datosNuevoPaciente[] = new double[8];
            datosNuevoPaciente[0] = Double.parseDouble(txtNumEmbarazos.getText());
            datosNuevoPaciente[1] = Double.parseDouble(txtGlucosa.getText());
            datosNuevoPaciente[2] = Double.parseDouble(txtPresionSanguinea.getText());
            datosNuevoPaciente[3] = Double.parseDouble(txtEspesorPiel.getText());
            datosNuevoPaciente[4] = Double.parseDouble(txtInsulina.getText());
            datosNuevoPaciente[5] = Double.parseDouble(txtBMI.getText());
            datosNuevoPaciente[6] = Double.parseDouble(txtPorcentajeDiabetesFamiliar.getText());
            datosNuevoPaciente[7] = Double.parseDouble(txtEdad.getText());
            if(neurona.diagnosticarPaciente(datosNuevoPaciente)==1){
                txaDiagnostico.setText(txaDiagnostico.getText() + "- El paciente " + txtNombres.getText() + " " + txtApellidos.getText() + " tiene diabetes\n");
            }else{
                txaDiagnostico.setText(txaDiagnostico.getText() + "- El paciente " + txtNombres.getText() + " " + txtApellidos.getText() + " no tiene diabetes\n");
            }
        }catch(NumberFormatException nfe){
            JOptionPane.showMessageDialog(rootPane, "Se deben ingresar números", "Error", ERROR);
        }catch(Exception e){
            JOptionPane.showMessageDialog(rootPane, "Error desconocido \n" + e, "Error",ERROR);
        }
    }//GEN-LAST:event_btnDianosticarActionPerformed

